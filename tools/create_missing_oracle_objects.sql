CREATE OR REPLACE VIEW XX_ETW.XX_DISL_SMENA_CHANGE_V AS
SELECT sc.SHIFT_CHANGE_ID,
       TO_CHAR(sc.SMENA_CHANGE_ID) SMENA_NUM,
       TO_CHAR(sc.STATUS_ID) STATUS_NAME,
       sc.STATUS_ID,
       sc.LAST_STATUS_ID,
       TO_CHAR(sc.CREATED_BY) CREATED_NAME,
       sc.CREATED_DATE,
       sc.LAST_UPDATE_DATE,
       st.SHORT_NAME STATION_SHORT_NAME,
       sc.STATION_ID,
       CAST(0 AS NUMBER) RAILWAYS_COUNT,
       CAST(0 AS NUMBER) PLACE_COUNT,
       CAST(0 AS NUMBER) INCORRECT_RAILWAYS,
       CAST(0 AS NUMBER) INCORRECT_PLACE,
       CAST(0 AS NUMBER) INCCORECT_TOTAL,
       sc.SIGN_OF_CHANGE,
       TO_CHAR(sc.SMENA_CHANGE_ID) SMENA_CHANGE,
       sc.SMENA_CHANGE_ID,
       sc.USER_CHANGE_ID,
       TO_CHAR(sc.USER_CHANGE_ID) USER_CHANGE,
       sc.DATE_CHANGE,
       sc.NOTE_CHANGE,
       sc.START_DATE_SHIFT,
       sc.DEV_CHANGE_TOTAL,
       sc.DEV_CHANGE_CORRECT,
       sc.DEV_CHANGE_INCCORECT,
       sc.SIGN_OF_RECEPTION,
       TO_CHAR(sc.USER_RECEPTION_ID) USER_RECEPTION,
       sc.DATE_RECEPTION,
       sc.SMENA_RECEPTION_ID,
       sc.DEV_RECEPTION_TOTAL,
       sc.DEV_RECEPTION_CORRECT,
       sc.DEV_RECEPTION_INCCORECT,
       sc.USER_RECEPTION_ID,
       sc.NOTE_RECEPTION,
       CAST(NULL AS VARCHAR2(4000)) L_MASS_SHORT_WAG
  FROM XX_ETW.XX_DISL_SHIFT_CHANGE sc
       LEFT JOIN XX_ETW.XX_DISL_STATIONS st ON st.STATION_ID = sc.STATION_ID;

