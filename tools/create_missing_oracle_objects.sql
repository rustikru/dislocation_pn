CREATE OR REPLACE VIEW XX_ETW.XX_DISL_SCALES_TYPE_V AS
SELECT ROW_NUMBER() OVER (ORDER BY TYPE) ID,
       TYPE NAME
  FROM (
        SELECT DISTINCT TYPE
          FROM XX_ETW.XX_DISL_CAR_SCALE
         WHERE TYPE IS NOT NULL
       );

