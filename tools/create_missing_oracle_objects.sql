CREATE OR REPLACE VIEW XX_ETW.XX_DISL_UNION_NAME_V AS
SELECT TO_CHAR(STATION_ID) ID,
       'station' TYPE,
       NAME,
       CAST(NULL AS VARCHAR2(100)) DISABLED
  FROM XX_ETW.XX_DISL_STATIONS
UNION ALL
SELECT TO_CHAR(RAILWAY_ID) ID,
       'railway' TYPE,
       RAILWAY_NUMBER NAME,
       DISABLED
  FROM XX_ETW.XX_DISL_RAILWAYS
UNION ALL
SELECT TO_CHAR(AREA_ID) ID,
       'area' TYPE,
       NAME,
       CAST(NULL AS VARCHAR2(100)) DISABLED
  FROM XX_ETW.XX_DISL_AREAS;

