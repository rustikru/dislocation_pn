<?php

function gu23_clean_text_for_oracle(string $text): string
{
    $text = str_replace(
        [
            '«', '»', '“', '”', '„', '‟',
            '‘', '’', '‚', '‛',
            '–', '—', '−',
            '…', '•',
            "\xC2\xA0", // неразрывный пробел
        ],
        [
            '"', '"', '"', '"', '"', '"',
            "'", "'", "'", "'",
            '-', '-', '-',
            '...', '-',
            ' ',
        ],
        $text
    );

    // 200B/200C/200D - невидимые пробелы, FEFF - BOM, 00AD - мягкий перенос.
    $text = preg_replace('/[\x{200B}\x{200C}\x{200D}\x{FEFF}\x{00AD}]/u', '', $text);

    // Убираем управляющие символы, кроме переноса строки и табуляции.
    $text = preg_replace('/[^\P{C}\r\n\t]/u', '', $text);

    return $text ?? '';
}
